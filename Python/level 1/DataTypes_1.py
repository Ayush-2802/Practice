# Write a Python program that accepts two numbers from the user and calculates their sum, difference, product, and quotient.
n = int(input("Enter a 1st number: "))
m = int(input("Enter a 2nd number: "))
print("The sum of the number is", n+m)
print("The difference of the number is", n-m)
print("The product of the number is", n*m)
print("The quotient of the number is", n/m)
print("The remainder of the number is", n%m)